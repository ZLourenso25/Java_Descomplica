//Implementação do comando do while. difença e que o do
//executa no final a condição fazendo assim com que o comando seja executado pelo menos uma vez
public class Principal13 {

	public static void main(String[] args) {
		
		int numero = 1;
		
		do {
			System.out.println(numero);
			
			numero++;//uso do comando incremento
			
		}while(numero <=5);
	
	}

}
