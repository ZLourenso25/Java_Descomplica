//Revisando os operadores relacionais
public class Principal08 {

	public static void main(String[] args) {
		
		int valor1, valor2;
		
		valor1 = 10;
		valor2 = 5;
		
		System.out.println("Valor1 = 10");
		System.out.println("Valor2 = 5");
		System.out.println();
		System.out.println("O valor 1 e igual ao valor 2: ");
		System.out.println(valor1 == valor2);
		System.out.println("O valor 1 e diferente ao valor 2: ");
		System.out.println(valor1 != valor2);
		System.out.println("O valor 1 e menor que o valor 2: ");
		System.out.println(valor1 < valor2);
		System.out.println("O valor 1 e maior que o valor 2: ");
		System.out.println(valor1 > valor2);
		System.out.println("O valor 1 e menor ou igual ao valor 2: ");
		System.out.println(valor1 <= valor2);
		System.out.println("O valor 1 e maior ou igual ao valor 2: ");
		System.out.println(valor1 >= valor2);
			}
}
