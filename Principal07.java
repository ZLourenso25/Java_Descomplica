//Operadores relacionais 
public class Principal07 {

	public static void main(String[] args) {
		
		int numero;
		
		numero = 6;
		
		System.out.println(numero > 4); //maior que 
		System.out.println(numero >= 4); //maior ou igual
		System.out.println(numero < 4); //menor que
		System.out.println(numero <= 4); // menor ou igual
		System.out.println(numero == 4); //Ele compara se são iguais
		System.out.println(numero != 4); //Ele é diferente de 
		
	}

}
