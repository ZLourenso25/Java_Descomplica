public class Principal02 {

	public static void main(String[] args) {
		
		byte x = 3; //O menor tipo inteiro é byte. Tem um valor mínimo de -128 e um valor máximo de 127 (inclusive). 
		short y = 4; //O tipo de dados curto é um inteiro de complemento de dois de 16 bits e assinado. Tem um valor mínimo de -32.768 e um valor máximo de 32.767 (inclusive).
		int z = 5; //O tipo inteiro mais comumente usado é int. É um tipo assinado de 32 bits que tem um intervalo de –2.147.483.648 a 2.147.483.647.
		long k = 6; //Long é um tipo assinado de 64 bits e é útil para aquelas ocasiões em que um tipo int não é grande o suficiente para armazenar o valor desejado.
		float u = 2; //O tipo float especifica um valor de precisão único que usa 32 bits de armazenamento.
		double w = 3; //A precisão dupla, como indicado pela palavra-chave double, usa 64 bits para armazenar um valor. 
		boolean t = true; //O tipo de dados booleano possui apenas dois valores possíveis: verdadeiro e falso. 
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(k);
		System.out.println(u);
		System.out.println(w);
		System.out.println(t);
		System.out.println();
		//operações aritmeticas 
		System.out.println(9+2);
		System.out.println(9-2);
		System.out.println(9*2);
		System.out.println(9/2);
		System.out.println(9.0/2);
	
		
	}

}
