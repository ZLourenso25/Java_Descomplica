//Explicando o comando if e o else
public class Principal09 {

	public static void main(String[] args) {
		
		int valor = 6; //int quer dizer numeros inteiros 
		
		if(valor == 8) { //if quer dizer "se" de uma condição se for verdadeira faz o proximo comando. obs.: depois do if não se usa ;
			
			System.out.println("Valor = 8");
		}
		else 
			if(valor>8) {
			
			System.out.println("Valor > 8");
		}
			else {
				System.out.println("Valor < 8");
			}
	}

}
