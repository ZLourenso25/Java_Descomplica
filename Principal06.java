//Exemplo de uso dos operadores com atribuição.
public class Principal06 {

	public static void main(String[] args) {

		int numero;
		
		numero = 6;
		
		numero += 1; //numero = numero + 1; numero++; ++numero
		numero += 2; //numero = numero + 2;
		
		System.out.println(numero);
		
		numero -= 2; //numero = numero - 2;
		
		System.out.println(numero);
		
		numero *= 2; //numero = numero * 2;
		
		System.out.println(numero);
		
		numero /= 2; //numero = numero / 2;
		
		System.out.println(numero);	
		
		numero %= 2; //numero = numero % 2;
		
		System.out.println(numero);
	}

}
